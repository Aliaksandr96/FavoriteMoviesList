import UIKit

extension UIFont {
    class func manropeRegularFont(withSize size: CGFloat) -> UIFont {
        return UIFont(name: "Manrope-Regular", size: size)!
    }
    class func manropeBoldFont(withSize size: CGFloat) -> UIFont {
        return UIFont(name: "Manrope-ExtraBold", size: size)!
    }
}


